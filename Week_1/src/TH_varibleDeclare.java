public class TH_varibleDeclare {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 20.5d;
        boolean b = true;
        char a = 'a';
        String s = "Hà Nội";
        System.out.println(i);
        System.out.println(f);
        System.out.println(d);
        System.out.println(b);
        System.out.println(a);
        System.out.println(s);
    }
}
